function _detectFromText(text) {
    const lower = (text || "").toLowerCase();
    let best = null, bestScore = 0;
    for (const { keywords, tool } of INTENT_KEYWORDS) {
        const score = keywords.reduce((s, kw) => s + (lower.includes(kw) ? 1 : 0), 0);
        if (score > bestScore) { bestScore = score; best = tool; }
    }
    return best;
}

function select(task) {
    // 1. Exact type match
    let tool = TOOL_MAP[task?.type];

    // 2. Free-text intent detection (when type is "ai" or missing)
    if (!tool || tool === "ai") {
        const fromText = _detectFromText(task?.input || task?.payload?.description || "");
        if (fromText) tool = fromText;
    }

    tool = tool || "ai";
    logManager.debug("ToolSelector.select", { taskType: task?.type, tool });
    return { tool, task };
}

function register(taskType, toolName) {
    if (!taskType || !toolName) throw new Error("register: taskType and toolName required");
    TOOL_MAP[taskType] = toolName;
    logManager.info("ToolSelector.register", { taskType, toolName });
}

function getMap() {
    return { ...TOOL_MAP };
}

module.exports = { select, register, getMap };
