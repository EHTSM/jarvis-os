    pm2_startup:      { exec: ["pm2 startup","pm2 save"], validation: ["systemctl status pm2-root shows active"], rollback: ["pm2 unstartup"] },
    backend_health:   { exec: ["curl http://localhost:5050/health"], validation: ["Returns HTTP 200 with {ok:true}"], rollback: ["Check pm2 logs for errors"] },
