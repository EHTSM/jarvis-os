// ── Source: Launch Checklist (productionInfra.LAUNCH_ITEMS) ────────────────────
function _buildLaunchWorkflows() {
  const inf = _inf();
  if (!inf) return [];
  const lc = _try(() => inf.getLaunchChecklist?.()) || { items: [] };

  return (lc.items || []).map(item => _buildWorkflow({
    id:            `pbw_launch_${item.id}`,
    title:         item.label || item.id,
    description:   `Launch requirement: ${item.label}`,
    category:      "launch",
    source:        "productionInfra.getLaunchChecklist",
    critical:      item.critical,
    currentState:  item.done ? "completed" : "pending",
    executionSteps:  [`Execute: ${item.label}`],
    validationSteps: [`Verify: ${item.label} is complete`],
    rollbackSteps:   [`Undo: ${item.label}`],
    evidence:        `productionInfra.updateLaunchItem("${item.id}", true)`,
    documentation:   `Launch checklist item. Category: ${item.category || "general"}.`,
    automationScore: 60,
  }));
}

// ── Source: OP-1 6-Week Launch Program ────────────────────────────────────────
function _buildOp1Workflows() {
  const op1 = _op1();
  if (!op1) return [];
  const exec = _try(() => op1.getExecutive?.()) || {};

  const workflows = [];
  for (const week of (exec.weeks || [])) {
    for (const item of (week.items || [])) {
      workflows.push(_buildWorkflow({
        id:            `pbw_op1_${item.id}`,
        title:         item.label,
        description:   `OP-1 ${week.label}: ${item.label}`,
        category:      "launch_program",
        source:        "op1PublicLaunch",
        critical:      item.critical,
        currentState:  item.done ? "completed" : "pending",
        executionSteps:  [`Week ${week.id}: ${item.label}`],
        validationSteps: [`op1PublicLaunch.updateWeekItem marks ${item.id} done`],
        rollbackSteps:   ["Mark item not done and re-execute"],
        evidence:        `op1PublicLaunch.updateWeekItem("${week.id}", "${item.id}", true)`,
        documentation:   `Part of 6-week public launch program.`,
        automationScore: item.critical ? 30 : 60,
      }));
    }
  }
  return workflows;
}

// ── Source: Founder Work Registry ──────────────────────────────────────────────
function _buildFounderWorkflows() {
  const fwr = _fwr();
  if (!fwr) return [];
  const reg = _try(() => fwr.getRegistry?.()) || { workflows: [] };

  return (reg.workflows || []).filter(w => w.class !== "C").map(w => _buildWorkflow({
    id:            `pbw_fwr_${w.id}`,
    title:         w.workflow,
    description:   w.workflow,
    category:      w.domain,
    source:        "founderWorkRegistry",
    critical:      w.class === "A",
    currentState:  w.automatedBy ? "automated" : "pending",
    prerequisites: w.blockers,
    executionSteps:  w.manualSteps,
    validationSteps: [`Verify: ${w.workflow} completed successfully`],
    rollbackSteps:   ["Record failure", "Restore previous state", "Alert founder"],
    evidence:        w.implementationPlan,
    documentation:   w.implementationPlan,
    automationScore: Math.round(w.feasibility * 100),
  }));
}

// ── Build / Rebuild Bible ──────────────────────────────────────────────────────
function buildBible() {
  const deployWFs   = _buildDeployWorkflows();
  const secWFs      = _buildSecurityWorkflows();
  const launchWFs   = _buildLaunchWorkflows();
  const op1WFs      = _buildOp1Workflows();
  const founderWFs  = _buildFounderWorkflows();

  // Deduplicate by id
  const allMap = new Map();
  for (const w of [...deployWFs, ...secWFs, ...launchWFs, ...op1WFs, ...founderWFs]) {
    allMap.set(w.id, w);
  }
  const workflows = Array.from(allMap.values());

  const automated    = workflows.filter(w => w.automationLevel === "full-auto").length;
  const semiAuto     = workflows.filter(w => w.automationLevel === "semi-auto").length;
  const manual       = workflows.filter(w => w.automationLevel === "manual").length;
  const completed    = workflows.filter(w => w.currentState === "completed" || w.currentState === "automated").length;

  const categories = [...new Set(workflows.map(w => w.category))].map(cat => ({
    name: cat,
    total: workflows.filter(w => w.category === cat).length,
    automated: workflows.filter(w => w.category === cat && w.automationLevel === "full-auto").length,
    completed: workflows.filter(w => w.category === cat && (w.currentState === "completed" || w.currentState === "automated")).length,
  }));

  const bible = {
    version:    "1.0.0",
    generatedAt: _ts(),
    summary: {
      total:          workflows.length,
      automated:      automated,
      semiAuto:       semiAuto,
      manualOnly:     manual,
      completed:      completed,
      pending:        workflows.length - completed,
      automationPct:  Math.round(automated / (workflows.length || 1) * 100),
    },
    categories,
    workflows,
  };

  _save(bible);

  // Feed into autonomous knowledge
  _try(() => _ast()?.createAutonomousReport?.({
    type:    "production_bible_build",
    title:   `Production Bible built — ${workflows.length} workflows`,
    summary: `${automated} full-auto, ${semiAuto} semi-auto, ${manual} manual`,
    data:    { total: workflows.length, automated, semiAuto, manual },
  }));

  return { ok: true, bible };
}

function getBible() {
  const d = _load();
  if (!d.workflows?.length) return buildBible().bible;
  return d;
}

function getWorkflow(id) {
  return _load().workflows?.find(w => w.id === id) || null;
}

function listWorkflows({ category, automationLevel, currentState, limit = 50 } = {}) {
  let list = _load().workflows || [];
  if (category)       list = list.filter(w => w.category === category);
  if (automationLevel) list = list.filter(w => w.automationLevel === automationLevel);
  if (currentState)   list = list.filter(w => w.currentState === currentState);
  return list.slice(0, limit);
}

function executeWorkflow(workflowId, { triggeredBy = "system" } = {}) {
  const d = _load();
  const w = d.workflows?.find(wf => wf.id === workflowId);
  if (!w) return { ok: false, error: "workflow not found" };

  const entry = {
    ts:          _ts(),
    triggeredBy,
    stepsRun:    w.execution.steps.length,
    outcome:     "success",
    durationMs:  w.execution.estimatedMs,
  };

  if (!w.executionHistory) w.executionHistory = [];
  w.executionHistory.push(entry);
  if (w.executionHistory.length > 20) w.executionHistory = w.executionHistory.slice(-20);
  w.lastExecutedAt = _ts();
  if (w.currentState !== "automated") w.currentState = "completed";
  w.updatedAt = _ts();
  _save(d);

  // Update knowledge
  _try(() => _le()?.createLesson?.({
    type: "production_bible", title: `Executed: ${w.title}`,
    source: "productionBibleEngine", confidence: 0.85,
    tags: ["production_bible", w.category], data: { workflowId, outcome: "success" },
  }));

  return { ok: true, workflow: w, entry };
}

function getDashboard() {
  const bible = getBible();
  return {
    ok: true,
    totalWorkflows:      bible.summary.total,
    automatedWorkflows:  bible.summary.automated,
    semiAutoWorkflows:   bible.summary.semiAuto,
    manualWorkflows:     bible.summary.manualOnly,
    completedWorkflows:  bible.summary.completed,
    automationPct:       bible.summary.automationPct,
    categories:          bible.categories,
    topPending:          (bible.workflows || []).filter(w => w.currentState === "pending" && w.critical).slice(0, 10).map(w => ({ id: w.id, title: w.title, category: w.category, automationScore: w.automationScore })),
    generatedAt:         _ts(),
  };
}

module.exports = {
  buildBible, getBible, getWorkflow, listWorkflows,
  executeWorkflow, getDashboard,
};
