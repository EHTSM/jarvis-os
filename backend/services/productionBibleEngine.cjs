    nginx_proxying:   { exec: ["Verify proxy_pass http://localhost:5050 in nginx.conf"], validation: ["curl https://app.ooplix.com/health returns 200"], rollback: ["Fix proxy_pass setting"] },
    ssl_cert:         { exec: ["certbot --nginx -d app.ooplix.com","Follow ACME challenge","Verify cert issued"], validation: ["openssl verify cert returns OK","HTTPS responds"], rollback: ["certbot delete --cert-name app.ooplix.com"] },
