    repo_cloned:      { exec: ["mkdir -p /opt/jarvis-os","git clone <repo_url> /opt/jarvis-os","cd /opt/jarvis-os && git status"], validation: ["ls /opt/jarvis-os/package.json exists"], rollback: ["rm -rf /opt/jarvis-os"] },
    deps_installed:   { exec: ["cd /opt/jarvis-os && npm install --omit=dev"], validation: ["node_modules/ exists","no critical npm errors"], rollback: ["rm -rf node_modules && npm install --omit=dev"] },
    env_configured:   { exec: ["Copy .env template","Fill in all required vars","Verify no placeholder values remain"], validation: ["productionInfra.auditEnvironment() returns no critical failures"], rollback: ["Restore previous .env"] },
